// Let and Const:
// Practice using Let and Const variables

function main() {
  const PI = Math.PI;
  let r = 6;

  const area = PI * r ** 2;
  const circumference = PI * (r * 2);

  console.log(area);
  console.log(circumference);
}

main();

//Output to console ->  113.09733552923255
//                      37.69911184307752
